    Megtekintheti, hogy melyik megyében és melyik helységben hány darab szélerőmű van szolgálatban jelenleg. 
    Emellett oldalunk lehetővé tesz néhány plusz funkciót is az Ön számára.
    Meg tudja tekinteni különböző pénznemek átváltási értékét egy adott napon, valamint egy megadott intervallumban egy kiválasztott devizapár 
    átváltási értékét is lekérheti, oldalunk egy táblázatban írja azt ki és egy grafikonon is megtekintheti az értékek változását.
    Az oldalon található egy íroldal is, ahol a legfrissebb híreket olvashatja a szélerőművekkel kapcsolatban és hozzászólásokat is fűzhet hozzájuk.
    Ez a funkció viszont regisztációhoz kötött. Ezt megteheti a belépés menüpont alatt. 
    Amennyiben viszont már korábban regisztált oldalunkra egy belépés után elérhetővé válik Ön sámára az említett funkció.</p>
